package math;

import java.util.Scanner;

public class Calcul {

	/**
	 * Addition Calcul
	 * @param a
	 * @param b
	 * @return
	 */
	public long addition(long a, long b) {
		return a + b;
	}
	
	/**
	 * Substraction Calcul
	 * @param a
	 * @param b
	 * @return
	 */
	public long substraction(long a, long b){
		return a - b;
	}
	
	public long multiplication(long a, long b){
		return a*b;
	}
	
	public long div(long a, long b){
		return a / b;
	}
}

